def reencode_h264(path):
    """OpenCV 'mp4v'(MPEG-4 Part 2) 출력은 브라우저 <video>에서 디코딩되지 않아
    검은 화면이 된다 -- ffmpeg로 H.264(yuv420p, +faststart)로 재인코딩해 같은
    경로에 덮어쓴다. 프레임 수는 1:1로 유지되므로 랜드마크 정렬에 영향 없음."""
    import shutil, subprocess
    ffmpeg = shutil.which('ffmpeg')
    if ffmpeg is None:
        try:
            import imageio_ffmpeg
            ffmpeg = imageio_ffmpeg.get_ffmpeg_exe()
        except ImportError:
            print(f"  [경고] ffmpeg 없음 -- {path}는 mp4v 그대로라 브라우저에서 재생 불가")
            return
    tmp = path + '.h264tmp.mp4'
    subprocess.run([ffmpeg, '-y', '-v', 'error', '-i', path,
                    '-c:v', 'libx264', '-pix_fmt', 'yuv420p', '-crf', '21',
                    '-movflags', '+faststart', '-an', tmp], check=True)
    os.replace(tmp, path)


def generate_occluded_video(video_path, frames_data, info, target_idx, output_path):
    cap = cv2.VideoCapture(video_path)
    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    out = cv2.VideoWriter(output_path, fourcc, info['fps'], (info['width'], info['height']))

    last_c, last_r = None, None
    idx = 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        f = frames_data[idx]
        if f["detected"] and "landmarks_px" in f:
            px = [(f["landmarks_px"][n]["x"], f["landmarks_px"][n]["y"],
                   f["landmarks_px"][n]["z"]) for n in LANDMARK_NAMES]
            last_c, last_r = occlusion_params(px, target_idx)
        if last_c and last_r:
            cv2.circle(frame, last_c, last_r, (0, 0, 0), -1)
        out.write(frame)
        idx += 1
    cap.release()
    out.release()
    reencode_h264(output_path)
    print(f"  {output_path} ({idx} frames)")


