def normalize_wrist(landmarks_px):
    pts = np.array([lm[:2] for lm in landmarks_px])
    wrist = pts[0]
    centered = pts - wrist
    scale = np.linalg.norm(centered[MIDDLE_MCP]) + 1e-8
    return centered / scale


def compute_joint_angle(landmarks_px, p, j, c):
    vp = np.array(landmarks_px[p][:2])
    vj = np.array(landmarks_px[j][:2])
    vc = np.array(landmarks_px[c][:2])
    v1 = vp - vj
    v2 = vc - vj
    cos_a = np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2) + 1e-8)
    return float(np.degrees(np.arccos(np.clip(cos_a, -1.0, 1.0))))


def compute_all_angles(landmarks_px):
    return {n: compute_joint_angle(landmarks_px, p, j, c)
            for n, (p, j, c) in JOINT_ANGLES.items()}


def occlusion_params(landmarks_px, target_idx):
    w = np.array(landmarks_px[WRIST][:2])

    mcp_indices = [1, 5, 9, 13, 17]
    mcp_avg = np.mean([np.array(landmarks_px[i][:2]) for i in mcp_indices], axis=0)

    base_dist = np.linalg.norm(mcp_avg - w)
    ratio = OCCLUSION_RATIOS[target_idx]
    r = int(base_dist * ratio / 2)

    # 가상 타겟: wrist에서 MCP 방향으로 ratio배 지점
    vt_x = w[0] + (mcp_avg[0] - w[0]) * ratio
    vt_y = w[1] + (mcp_avg[1] - w[1]) * ratio

    # 중심 = wrist와 가상 타겟의 중점
    cx = int((w[0] + vt_x) / 2)
    cy = int((w[1] + vt_y) / 2)

    return (cx, cy), r

def classify_visibility(landmarks_px, center, radius):
    result = {}
    for i, name in enumerate(LANDMARK_NAMES):
        lm = landmarks_px[i]
        dist = np.sqrt((lm[0]-center[0])**2 + (lm[1]-center[1])**2)
        result[name] = "occluded" if dist <= radius else "visible"
    return result


def compare_poses(orig_px, test_px, visibility=None):
    orig_angles = compute_all_angles(orig_px)
    test_angles = compute_all_angles(test_px)
    angle_errors = {k: abs(orig_angles[k] - test_angles[k]) for k in JOINT_ANGLES}

    orig_norm = normalize_wrist(orig_px)
    test_norm = normalize_wrist(test_px)
    coord_errors = {LANDMARK_NAMES[i]: float(np.linalg.norm(orig_norm[i] - test_norm[i]))
                    for i in range(21)}

    result = {
        "angle_errors": angle_errors,
        "angle_error_mean": float(np.mean(list(angle_errors.values()))),
        "coord_errors": coord_errors,
        "coord_error_mean": float(np.mean(list(coord_errors.values()))),
    }

    if visibility:
        vis_ae, occ_ae, vis_ce, occ_ce = [], [], [], []
        for name, (p, j, c) in JOINT_ANGLES.items():
            joint_name = LANDMARK_NAMES[j]
            err = angle_errors[name]
            if visibility.get(joint_name) == "occluded":
                occ_ae.append(err)
            else:
                vis_ae.append(err)
        for i, name in enumerate(LANDMARK_NAMES):
            err = coord_errors[name]
            if visibility.get(name) == "occluded":
                occ_ce.append(err)
            else:
                vis_ce.append(err)
        result["visible_angle_error_mean"] = float(np.mean(vis_ae)) if vis_ae else None
        result["occluded_angle_error_mean"] = float(np.mean(occ_ae)) if occ_ae else None
        result["visible_coord_error_mean"] = float(np.mean(vis_ce)) if vis_ce else None
        result["occluded_coord_error_mean"] = float(np.mean(occ_ce)) if occ_ce else None

    return result


