def extract_landmarks_from_video(video_path, confidence=0.5):
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        raise FileNotFoundError(f"영상을 열 수 없음: {video_path}")

    w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    fps = cap.get(cv2.CAP_PROP_FPS)
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    info = {"width": w, "height": h, "fps": fps, "total_frames": total, "source": video_path}

    mp_hands = mp_module.solutions.hands
    hands = mp_hands.Hands(static_image_mode=False, max_num_hands=1,
                           min_detection_confidence=confidence,
                           min_tracking_confidence=confidence)
    frames = []
    idx = 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        result = hands.process(rgb)
        entry = {"frame_idx": idx, "detected": False, "confidence": None}
        if result.multi_hand_landmarks:
            hand = result.multi_hand_landmarks[0]
            px_dict, norm_dict = {}, {}
            for i, lm in enumerate(hand.landmark):
                name = LANDMARK_NAMES[i]
                px_dict[name] = {"x": lm.x * w, "y": lm.y * h, "z": lm.z}
                norm_dict[name] = {"x": lm.x, "y": lm.y, "z": lm.z}
            entry["detected"] = True
            entry["landmarks_px"] = px_dict
            entry["landmarks_norm"] = norm_dict
            if result.multi_handedness:
                entry["confidence"] = result.multi_handedness[0].classification[0].score
        frames.append(entry)
        idx += 1
        if idx % 50 == 0:
            det = sum(1 for f in frames if f["detected"])
            print(f"  {idx}/{total} ({det/idx*100:.0f}% 인식)")

    cap.release()
    hands.close()
    det = sum(1 for f in frames if f["detected"])
    print(f"  완료: {det}/{len(frames)} 프레임 인식 ({det/len(frames)*100:.1f}%)")
    return frames, info


