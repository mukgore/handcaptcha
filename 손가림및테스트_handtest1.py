def compute_jitter(landmarks_list):
    prev = None
    jitters = []
    for px in landmarks_list:
        if px is None:
            prev = None
            continue
        norm = normalize_wrist(px)
        if prev is not None:
            jitters.append(np.linalg.norm(norm - prev, axis=1))
        prev = norm
    if not jitters:
        return None
    arr = np.array(jitters)
    return {
        "overall_mean": float(np.mean(arr)),
        "overall_std": float(np.std(arr)),
        "per_landmark": {LANDMARK_NAMES[i]: float(np.mean(arr[:, i])) for i in range(21)},
    }


def _summarize_errors(error_list, label):
    if not error_list:
        return None
    angles = [e["angle_error_mean"] for e in error_list]
    coords = [e["coord_error_mean"] for e in error_list]
    vis_angles = [e["visible_angle_error_mean"] for e in error_list if e.get("visible_angle_error_mean") is not None]
    occ_angles = [e["occluded_angle_error_mean"] for e in error_list if e.get("occluded_angle_error_mean") is not None]
    return {
        "n_frames": len(error_list),
        "angle_error_mean": float(np.mean(angles)),
        "coord_error_mean": float(np.mean(coords)),
        "visible_angle_mean": float(np.mean(vis_angles)) if vis_angles else None,
        "occluded_angle_mean": float(np.mean(occ_angles)) if occ_angles else None,
    }


