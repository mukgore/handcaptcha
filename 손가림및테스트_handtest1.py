def compute_movements(frames_data):
    movements = []
    prev_px = None
    for frame in frames_data:
        if not frame["detected"] or "landmarks_px" not in frame:
            movements.append(np.nan)
            prev_px = None
            continue
        curr_px = np.array([[frame["landmarks_px"][n]["x"],
                             frame["landmarks_px"][n]["y"]]
                            for n in LANDMARK_NAMES])
        if prev_px is not None:
            movements.append(float(np.sum(np.linalg.norm(curr_px - prev_px, axis=1))))
        else:
            movements.append(0.0)
        prev_px = curr_px
    return np.array(movements)


def segment_video(frames_data, fps, smooth_window=5, static_percentile=30.0, min_static_sec=0.3):
    movements = compute_movements(frames_data)
    valid = ~np.isnan(movements)
    if valid.sum() < 2:
        return None

    kernel = np.ones(smooth_window) / smooth_window
    smoothed = np.convolve(np.nan_to_num(movements, nan=0.0), kernel, mode='same')
    threshold = np.percentile(smoothed[valid], static_percentile)
    labels = ["static" if s <= threshold else "dynamic" for s in smoothed]

    min_frames = max(int(min_static_sec * fps), 1)
    static_segs, dynamic_segs = [], []
    seg_start = 0
    for i in range(1, len(labels)):
        if i == len(labels) - 1 or labels[i] != labels[seg_start]:
            end = i + 1 if i == len(labels) - 1 else i
            if labels[seg_start] == "static" and (end - seg_start) >= min_frames:
                static_segs.append((seg_start, end))
            elif labels[seg_start] == "dynamic":
                dynamic_segs.append((seg_start, end))
            seg_start = i

    return {
        "smoothed_movements": smoothed.tolist(),
        "threshold": float(threshold),
        "static_segments": static_segs,
        "dynamic_segments": dynamic_segs,
        "frame_labels": labels,
    }


def select_pause_frames(static_segments, total_frames, n=3):
    if not static_segments:
        return []
    if len(static_segments) <= n:
        return [{"segment": [s, e], "pause_frame": (s+e)//2}
                for s, e in static_segments]

    section = total_frames / n
    selected = []
    for i in range(n):
        s_start, s_end = i * section, (i + 1) * section
        s_mid = (s_start + s_end) / 2
        best, best_dist = None, float('inf')
        for seg in static_segments:
            seg_mid = (seg[0] + seg[1]) / 2
            if s_start <= seg_mid <= s_end:
                d = abs(seg_mid - s_mid)
                if d < best_dist:
                    best_dist, best = d, seg
        if best is None:
            for seg in static_segments:
                d = abs((seg[0]+seg[1])/2 - s_mid)
                if d < best_dist:
                    best_dist, best = d, seg
        if best:
            pf = (best[0] + best[1]) // 2
            if not any(x["pause_frame"] == pf for x in selected):
                selected.append({"segment": list(best), "pause_frame": pf})

    selected.sort(key=lambda x: x["pause_frame"])
    return selected


