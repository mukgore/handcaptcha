bad_pcts = []
fail_pcts = []

for lv, target_idx in OCCLUSION_LEVELS.items():
    video_path = os.path.join(output_dir, f"{lv}.mp4")
    if not os.path.exists(video_path):
        continue

    print(f"[{lv}] Analyzing...")
    occ_frames_l, _ = extract_landmarks_from_video(video_path)

    total = len(occ_frames_l)
    n_good = 0
    n_bad = 0
    n_fail = 0

    for occ_f in occ_frames_l:
        idx = occ_f["frame_idx"]
        if idx >= len(orig_frames):
            break

        orig_f = orig_frames[idx]

        # 가림 영상에서 손 인식 실패
        if not occ_f["detected"]:
            n_fail += 1
            continue

        # 원본도 인식 안 된 프레임은 비교 불가 → 제외
        if not orig_f["detected"]:
            continue

        orig_px = [(orig_f["landmarks_px"][n]["x"],
                    orig_f["landmarks_px"][n]["y"],
                    orig_f["landmarks_px"][n]["z"]) for n in LANDMARK_NAMES]
        occ_px = [(occ_f["landmarks_px"][n]["x"],
                   occ_f["landmarks_px"][n]["y"],
                   occ_f["landmarks_px"][n]["z"]) for n in LANDMARK_NAMES]

        angles_orig = compute_all_angles(orig_px)
        angles_occ = compute_all_angles(occ_px)
        mean_err = np.mean([abs(angles_orig[k] - angles_occ[k]) for k in JOINT_ANGLES])

        if mean_err < ANGLE_THRESHOLD:
            n_good += 1
        else:
            n_bad += 1

    print(f"  Total: {total}, Good: {n_good}, Bad: {n_bad}, Fail: {n_fail}")

    if total > 0:
        good_pcts.append(n_good / total * 100)
        bad_pcts.append(n_bad / total * 100)
        fail_pcts.append(n_fail / total * 100)
    else:
        good_pcts.append(0)
        bad_pcts.append(0)
        fail_pcts.append(0)

    level_short.append(lv.replace("level", "L").replace("_", "\n"))

x = np.arange(len(level_short))
